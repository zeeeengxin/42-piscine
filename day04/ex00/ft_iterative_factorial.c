int	ft_iterative_factorial(int nb)
{
	int i;

	if (nb == 0 || nb == 1)
	{
		return (1);
	}
	if (nb > 1 && nb <= 12)
	{
		i = nb - 1;
		while (i > 1)
		{
			nb = nb * i;
			i = i - 1;
		}
		return (nb);
	}
	return (0);
}
