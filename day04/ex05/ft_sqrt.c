int	ft_sqrt(int nb)
{
	int	root;

	root = 0;
	if (nb < 0)
	{
		return (0);
	}
	while (root < 46341)
	{
		if (nb == root * root)
		{
			return (root);
		}
		root++;
	}
	return (0);
}
